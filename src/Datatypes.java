public class Datatypes {
    public static void main(String[] args){
        int min = Integer.MIN_VALUE;
        int max = Integer.MAX_VALUE;
        byte min_byte = Byte.MIN_VALUE;
        byte max_byte = Byte.MAX_VALUE;
        System.out.println(min);
        System.out.println(max);
        System.out.println(min_byte);
        System.out.println(max_byte);
        long mylong = 100L;
        long min_long = Long.MIN_VALUE;
        long max_long = Long.MAX_VALUE;
        System.out.println(min_long);
        System.out.println(max_long);
    }
}
